import os
import json
from datetime import datetime, timezone
import pandas as pd


DATA_PATH = os.getenv("DATA_PATH", "./data/transactions.parquet")
METADATA_PATH = os.getenv("METADATA_PATH", "./data/metadata.json")
OUT_REPORT = os.getenv("OUT_REPORT", "./data/qa_report.json")

# Hard requirements
REQUIRED_COLUMNS = {
    "transaction_id": "object",
    "timestamp": "datetime64[ns]",
    "user_id": "object",
    "merchant_id": "object",
    "device_id": "object",
    "amount": "float",
    "country": "object",
    "is_fraud": "int",
}

ALLOWED_COUNTRIES = {"US", "CA", "UK"}


def _fail(errors: list[str], report: dict):
    report["status"] = "FAIL"
    report["errors"] = errors
    with open(OUT_REPORT, "w", encoding="utf-8") as f:
        json.dump(report, f, indent=2, default=str)
    raise SystemExit("\n".join(errors))


def main():
    report = {
        "status": "UNKNOWN",
        "data_path": DATA_PATH,
        "metadata_path": METADATA_PATH if os.path.exists(METADATA_PATH) else None,
        "generated_at_utc": datetime.now(timezone.utc).isoformat(),
        "checks": {},
        "errors": [],
        "warnings": [],
    }

    # Load metadata
    metadata = {}
    if os.path.exists(METADATA_PATH):
        with open(METADATA_PATH, "r", encoding="utf-8") as f:
            metadata = json.load(f)

    # Load dataset
    df = pd.read_parquet(DATA_PATH)

    errors = []
    warnings = []

    # Schema check
    missing = [c for c in REQUIRED_COLUMNS.keys() if c not in df.columns]
    if missing:
        errors.append(f"Missing required columns: {missing}")

    # Types (soft check in case parquet coerces)
    report["checks"]["row_count"] = int(len(df))

    # Null checks for key fields
    key_fields = ["transaction_id", "timestamp", "user_id", "merchant_id", "device_id"]
    nulls = {c: int(df[c].isna().sum()) for c in key_fields if c in df.columns}
    report["checks"]["null_counts"] = nulls
    for c, n in nulls.items():
        if n > 0:
            errors.append(f"Nulls found in key field '{c}': {n}")

    # Uniqueness check for transaction_id
    if "transaction_id" in df.columns:
        n_unique = int(df["transaction_id"].nunique(dropna=False))
        report["checks"]["transaction_id_unique"] = n_unique
        if n_unique != len(df):
            errors.append(f"transaction_id is not unique: unique={n_unique}, rows={len(df)}")

    # Timestamp range checks
    if "timestamp" in df.columns:
        # Ensure datetime
        if not pd.api.types.is_datetime64_any_dtype(df["timestamp"]):
            errors.append("timestamp is not a datetime dtype after reading parquet.")
        else:
            ts_min = df["timestamp"].min()
            ts_max = df["timestamp"].max()
            report["checks"]["timestamp_min"] = ts_min.isoformat()
            report["checks"]["timestamp_max"] = ts_max.isoformat()

            # Validate against metadata if available
            if metadata.get("start_date") and metadata.get("window_days"):
                start = pd.to_datetime(metadata["start_date"])
                end = start + pd.Timedelta(days=int(metadata["window_days"]))
                # allow a tiny tolerance for inclusive boundaries
                if ts_min < start - pd.Timedelta(seconds=1) or ts_max > end + pd.Timedelta(seconds=1):
                    errors.append(
                        f"Timestamp range outside expected window. "
                        f"expected=[{start}, {end}] got=[{ts_min}, {ts_max}]"
                    )
            else:
                warnings.append("No metadata start_date/window_days found; timestamp window not validated.")

    # Amount check
    if "amount" in df.columns:
        if (df["amount"] < 0).any():
            n_neg = int((df["amount"] < 0).sum())
            errors.append(f"Negative amounts found: {n_neg}")

        pct = df["amount"].quantile([0.5, 0.9, 0.99]).to_dict()
        report["checks"]["amount_quantiles"] = {str(k): float(v) for k, v in pct.items()}

        # Heuristic: long tail should have 99th > 90th > median
        if not (pct[0.99] > pct[0.9] > pct[0.5]):
            warnings.append("Amount quantiles do not show expected long-tail shape.")

    # Country domain checks
    if "country" in df.columns:
        bad_countries = sorted(set(df["country"].dropna().unique()) - ALLOWED_COUNTRIES)
        report["checks"]["distinct_countries"] = sorted(set(df["country"].dropna().unique()))
        if bad_countries:
            errors.append(f"Unexpected country values: {bad_countries}")

    # Fraud rate checks
    if "is_fraud" in df.columns:
        # Ensure only 0/1
        bad_labels = sorted(set(df["is_fraud"].dropna().unique()) - {0, 1})
        if bad_labels:
            errors.append(f"Unexpected is_fraud values (expected 0/1): {bad_labels}")

        fraud_rate_actual = float(df["is_fraud"].mean())
        report["checks"]["fraud_rate_actual"] = fraud_rate_actual

        fraud_rate_config = metadata.get("fraud_rate_config")
        if fraud_rate_config is not None:
            fraud_rate_config = float(fraud_rate_config)
            report["checks"]["fraud_rate_config"] = fraud_rate_config

            # Allow small tolerance because of rounding in sample size
            tol = 0.002  # +/- 0.2%
            if abs(fraud_rate_actual - fraud_rate_config) > tol:
                warnings.append(
                    f"Fraud rate differs from config beyond tolerance: "
                    f"actual={fraud_rate_actual:.4f}, config={fraud_rate_config:.4f}, tol={tol}"
                )
        else:
            warnings.append("No fraud_rate_config in metadata; cannot compare fraud rate to expected value.")

    # Row count check vs metadata
    if metadata.get("num_transactions") is not None:
        expected_rows = int(metadata["num_transactions"])
        if len(df) != expected_rows:
            errors.append(f"Row count mismatch: expected={expected_rows}, got={len(df)}")
        report["checks"]["expected_rows"] = expected_rows

    # Finalize
    report["warnings"] = warnings

    if errors:
        _fail(errors, report)

    report["status"] = "PASS"
    with open(OUT_REPORT, "w", encoding="utf-8") as f:
        json.dump(report, f, indent=2, default=str)

    print(f"[QA] PASS. Wrote report to {OUT_REPORT}")


if __name__ == "__main__":
    main()