def connect_postgres_with_retry():
    attempts = 0
    while True:
        try:
            conn = psycopg2.connect(pg_dsn())
            conn.autocommit = False
            logger.info(
                "Connected to Postgres at %s:%s db=%s user=%s",
                PGHOST,
                PGPORT,
                PGDATABASE,
                PGUSER,
            )
            return conn
        except Exception as exc:
            attempts += 1
            logger.warning(
                "Postgres connection failed (attempt %s/%s): %s",
                attempts,
                PG_CONNECT_RETRIES,
                exc,
            )
            if attempts >= PG_CONNECT_RETRIES:
                raise
            time.sleep(PG_CONNECT_RETRY_SLEEP_SEC)


