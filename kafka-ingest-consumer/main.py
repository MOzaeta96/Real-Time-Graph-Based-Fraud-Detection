def insert_batch(conn, records: list[tuple]) -> int:
    if not records:
        return 0

    with get_cursor(conn) as cur:
        execute_values(
            cur,
            INSERT_SQL,
            records,
            page_size=min(BATCH_SIZE, len(records)),
        )
    conn.commit()
    return len(records)


