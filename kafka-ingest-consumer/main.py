def parse_event_time(value: Any) -> datetime:
    if isinstance(value, datetime):
        dt = value
    elif isinstance(value, str):
        normalized = value.replace("Z", "+00:00")
        dt = datetime.fromisoformat(normalized)
    else:
        raise ValueError(f"Invalid event_time type: {type(value)}")

    # Normalize timezone-aware timestamps to naive UTC for Postgres TIMESTAMP.
    if dt.tzinfo is not None:
        dt = dt.astimezone(timezone.utc).replace(tzinfo=None)
    return dt


def normalize_event(event: dict[str, Any]) -> tuple:
    missing = REQUIRED_FIELDS - set(event.keys())
    if missing:
        raise ValueError(f"Missing required field(s): {sorted(missing)}")

    event_id = str(event["event_id"]).strip()
    transaction_id = str(event["transaction_id"]).strip()
    user_id = str(event["user_id"]).strip()
    merchant_id = str(event["merchant_id"]).strip()
    device_id = str(event["device_id"]).strip()
    country = str(event["country"]).strip()

    if not event_id:
        raise ValueError("event_id is empty")
    if not transaction_id:
        raise ValueError("transaction_id is empty")
    if not user_id:
        raise ValueError("user_id is empty")
    if not merchant_id:
        raise ValueError("merchant_id is empty")
    if not device_id:
        raise ValueError("device_id is empty")
    if not country:
        raise ValueError("country is empty")

    event_time = parse_event_time(event["event_time"])

    amount = float(event["amount"])
    if amount < 0:
        raise ValueError("amount must be >= 0")

    is_fraud = int(event["is_fraud"])
    if is_fraud not in (0, 1):
        raise ValueError("is_fraud must be 0 or 1")

    return (
        event_id,
        event_time,
        event_time,
        transaction_id,
        user_id,
        merchant_id,
        device_id,
        amount,
        country,
        is_fraud,
    )


