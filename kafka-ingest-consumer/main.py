def main() -> None:
    logger.info("Starting kafka-ingest-consumer...")
    logger.info(
        "Config: topic=%s group_id=%s pg=%s:%s/%s batch_size=%s dlq=%s",
        KAFKA_TOPIC,
        KAFKA_GROUP_ID,
        PGHOST,
        PGPORT,
        PGDATABASE,
        BATCH_SIZE,
        DLQ_ENABLED,
    )

    conn = connect_postgres_with_retry()
    if CREATE_TABLE_IF_MISSING:
        ensure_table_exists(conn)

    consumer = create_consumer()
    dlq_producer = create_dlq_producer()

    total_seen = 0
    total_valid = 0
    total_inserted = 0
    total_invalid = 0

    try:
        while RUNNING:
            polled = consumer.poll(timeout_ms=KAFKA_POLL_TIMEOUT_MS, max_records=KAFKA_MAX_RECORDS_PER_POLL)

            if not polled:
                continue

            batch_records: list[tuple] = []
            invalid_in_poll = 0

            for topic_partition, messages in polled.items():
                for msg in messages:
                    total_seen += 1
                    raw_event = msg.value

                    try:
                        record = normalize_event(raw_event)
                        batch_records.append(record)
                        total_valid += 1
                    except Exception as exc:
                        invalid_in_poll += 1
                        total_invalid += 1
                        logger.warning(
                            "Invalid event skipped at partition=%s offset=%s: %s",
                            msg.partition,
                            msg.offset,
                            exc,
                        )
                        publish_to_dlq(
                            producer=dlq_producer,
                            raw_event=raw_event,
                            error_message=str(exc),
                            kafka_key=msg.key,
                        )

            if batch_records:
                try:
                    inserted_count = insert_batch(conn, batch_records)
                    total_inserted += inserted_count

                    if not KAFKA_ENABLE_AUTO_COMMIT:
                        consumer.commit()

                    logger.info(
                        "Committed batch: seen=%s valid=%s invalid=%s inserted=%s totals(seen=%s valid=%s invalid=%s inserted=%s)",
                        len(batch_records) + invalid_in_poll,
                        len(batch_records),
                        invalid_in_poll,
                        inserted_count,
                        total_seen,
                        total_valid,
                        total_invalid,
                        total_inserted,
                    )
                except Exception as exc:
                    conn.rollback()
                    logger.exception("Batch insert failed; offsets not committed: %s", exc)
                    if EXIT_ON_ERROR:
                        raise
                    time.sleep(2)

    except KeyboardInterrupt:
        logger.info("Interrupted by keyboard.")
    finally:
        logger.info(
            "Shutting down. Final totals: seen=%s valid=%s invalid=%s inserted=%s",
            total_seen,
            total_valid,
            total_invalid,
            total_inserted,
        )

        try:
            if dlq_producer is not None:
                dlq_producer.flush()
                dlq_producer.close()
        except Exception:
            logger.exception("Error while closing DLQ producer.")

        try:
            consumer.close()
        except Exception:
            logger.exception("Error while closing Kafka consumer.")

        try:
            conn.close()
        except Exception:
            logger.exception("Error while closing Postgres connection.")


if __name__ == "__main__":
    main()